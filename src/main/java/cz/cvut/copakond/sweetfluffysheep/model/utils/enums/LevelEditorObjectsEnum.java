package cz.cvut.copakond.sweetfluffysheep.model.utils.enums;

/**
 * Enum representing the actions of objects that can be placed in the level editor.
 * The order of the enum values is important, as it determines the order in which they are displayed in the level editor.
 */
public enum LevelEditorObjectsEnum {
    TILE,
    REMOVETILE,
    WOLF,
    COIN,
    FREEZE,
    RAGE,
    START,
    GOAL,
    ROTATE,
    DESTROY,
    EMPTY,
}

